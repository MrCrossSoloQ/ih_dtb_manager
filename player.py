class Player:
    def __init__(self, surname = None, last_name = None, nationality = None, league_id = None, player_position = None, date_of_birth = None, team_id = None, url = None, jersey_number = None, player_shorted_name = None, name_variants = None):
        self.surname = surname
        self.last_name = last_name
        self.nationality = nationality
        self.league_id = league_id
        self.player_position = player_position
        self.date_of_birth = date_of_birth
        self.team_id = team_id
        self.url = url
        self.jersey_number = jersey_number
        self.player_shorted_name = player_shorted_name
        self.name_variants = name_variants
